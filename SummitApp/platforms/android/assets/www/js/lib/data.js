function renderProductSummit(products) {
    var html = '<div class="lista_verde no-line"><ul class="summitweb">';

    for (var i = 0; i < products.length; i++) {
        var table = '<table  class="tablesummit" data-id="' + products[i].pid + '">';

        if (products[i].c != null && products[i].c != '')
            table += '<tr><th>Cultivo</th><td>' + products[i].c + '</td></tr>';
        if (products[i].i != null && products[i].i != '')
            table += '<tr><th>Ingrediente activo</th><td>' + products[i].i + '<td></tr>';

        if (products[i].e != null && products[i].e != '')
            table += '<tr><th>Enfermedad</th><td>' + products[i].e + '</td></tr>';

        if (products[i].s != null && products[i].s != '')
            table += '<tr><th>Segmento</th><td>' + products[i].s + '</td></tr>';

        if (products[i].ix != null && products[i].ix != '')
            table += '<tr><th>Insecto</th><td>' + products[i].ix + '</td></tr>';

        table += '</table>';

        html += '<li><svg class="icon icon-caret-right"><use xmlns:xlink="http://www.w3.org/1999/xlink" xlink:href="#icon-caret-right"></use></svg><span class="title">' + products[i].p + '</span>' + table + '</li>';
    }

    html += '</ul></div>';

    return html;

}
